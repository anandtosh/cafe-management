                        <mark>PAN CARD</mark>
                        PAN Card (Permanent Account Number) is provided by Income Tax Department to
                        every Income Tax payers.
                        The PAN Card is a 10-character long alpha-numeric unique identifier. An example of a
                        PAN Card number would be in the form of AAAPL1234C.
                        The PAN is mandatory for a majority of financial transactions such as opening a bank
                        account, receiving taxable salary or professional fees, purchase of foreign currency,
                        bank deposits above Rs. 50,000, purchase and sale of immovable properties, vehicles
                        etc. Quoting the PAN is mandatory when filing Income Tax returns, tax deduction at
                        source, or any other communication with Income Tax Department.
                        The primary objective of PAN is to use a universal identification key to track all financial
                        transactions and to avoid tax fudging by keeping track of monetary transactions,
                        especially those of high-net-worth individuals who can impact the economy.
                        The PAN is unique to each applicant and is valid for the lifetime of the holder,
                        throughout India. Since a PAN is linked to an individual, a point worth to mention is that
                        the PAN code is therefore not affected by any change of address.
